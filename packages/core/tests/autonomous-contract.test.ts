    expect(await exists(rootDir, ".claude/rules/style.md")).toBe(true);
    expect(await exists(rootDir, ".claude/scripts/check.sh")).toBe(true);
    expect(await exists(rootDir, ".claude/skills/atom-example/SKILL.md")).toBe(true);
    expect(await exists(rootDir, ".git/hooks/pre-commit")).toBe(true);
    expect(hook).toContain("generated by harness; do not edit");
    expect(hook).toContain("npm test");
    expect(firstSessionCommand.command).toBe("echo session");
    expect(firstSessionCommand.timeout).toBe(3);
    expect(firstSessionCommand.statusMessage).toBe("Starting autonomous run");
    expect(Object.keys(mcpServers)).toEqual(["alpha", "beta"]);
    expect(openaiMarketplace.source).toBe("github:openai/codex-plugins");
