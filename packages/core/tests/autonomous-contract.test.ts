    await writeText(rootDir, ".codex/config.toml.template", 'profile = "project"\n');
    await writeText(rootDir, "agents/reviewer.md", "---\ndescription: Review code\n---\nReview generated work.\n");
    await writeText(rootDir, "commands/review.md", "Run the review workflow.\n");
    await writeText(rootDir, "docs/README.md", "# Active docs\n");
    await writeText(rootDir, "docs/archive/old.md", "# Archived run\n");
