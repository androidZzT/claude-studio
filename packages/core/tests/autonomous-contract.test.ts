    expect(firstPlugin.plugin).toBe("browser-use@openai");
    expect(firstPlugin.scope).toBe("project");
    expect(firstPlugin.enabled).toBe(true);
    expect(asArray(permissions.deny)).toEqual(["Read(./docs/archive)", "Read(./docs/archive/**)"]);
    expect(filesystem.denyRead).toEqual(["./docs/archive", "./docs/archive/**"]);
    expect(manifestByPath.get(".claude/settings.json")?.kind).toBe("partial-json");
    expect(manifestByPath.get(".claude/settings.json")?.owned_keys).toEqual([
      "hooks",
      "marketplaces",
      "mcpServers",
      "permissions",
      "plugins",
      "sandbox"
    ]);
    expect(manifestByPath.get(".git/hooks/pre-commit")?.mode).toBe(0o755);
    expect(changedPaths(afterSyncDiff)).toEqual([]);
    expect(afterSyncDiff.unchanged.map((entry) => entry.path).sort((left, right) => left.localeCompare(right))).toEqual(manifestPaths(manifest));
