    expect(await exists(rootDir, ".claude/docs/README.md")).toBe(true);
    expect(await exists(rootDir, ".claude/docs/archive/old.md")).toBe(false);
    expect(await exists(rootDir, ".claude/metrics/events.schema.md")).toBe(true);
