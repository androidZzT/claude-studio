    const sessionStartCommands = asArray(firstSessionStart.hooks);
    const firstSessionCommand = asObject(sessionStartCommands[0]);
    const mcpServers = asObject(settings.mcpServers);
    const marketplaces = asObject(settings.marketplaces);
    const openaiMarketplace = asObject(marketplaces.openai);
    const plugins = asArray(settings.plugins);
    const firstPlugin = asObject(plugins[0]);
    const permissions = asObject(settings.permissions);
    const sandbox = asObject(settings.sandbox);
    const filesystem = asObject(sandbox.filesystem);
    const hook = await readText(rootDir, ".git/hooks/pre-commit");
    const manifestByPath = new Map(manifest.files.map((entry) => [entry.path, entry]));
    const afterSyncDiff = await runDiff(rootDir);

    expect(changedPaths(syncResult)).toContain(".claude/settings.json");
    expect(await exists(rootDir, "AGENTS.md")).toBe(true);
    expect(await exists(rootDir, ".codex/config.toml")).toBe(true);
    expect(await exists(rootDir, "CLAUDE.md")).toBe(true);
