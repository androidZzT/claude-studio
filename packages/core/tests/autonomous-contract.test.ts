import { access, chmod, mkdir, mkdtemp, readFile, writeFile } from "node:fs/promises";
import os from "node:os";
import path from "node:path";

import { describe, expect, it } from "vitest";

import { loadManifest, runDiff, runSync } from "../src/index.js";
import type { ManifestFile, ReconcileResult } from "../src/index.js";

async function createWorkspace(prefix: string): Promise<string> {
  return mkdtemp(path.join(os.tmpdir(), prefix));
}

async function exists(rootDir: string, relativePath: string): Promise<boolean> {
  try {
    await access(path.join(rootDir, relativePath));
    return true;
  } catch (error) {
    if ((error as NodeJS.ErrnoException).code === "ENOENT") {
      return false;
    }

    throw error;
  }
}

async function readJson(rootDir: string, relativePath: string): Promise<Record<string, unknown>> {
  return JSON.parse(await readText(rootDir, relativePath)) as Record<string, unknown>;
}

async function readText(rootDir: string, relativePath: string): Promise<string> {
  return readFile(path.join(rootDir, relativePath), "utf8");
}

async function writeText(rootDir: string, relativePath: string, content: string): Promise<void> {
  const filePath = path.join(rootDir, relativePath);
  await mkdir(path.dirname(filePath), { recursive: true });
  await writeFile(filePath, content, "utf8");
}

function asObject(value: unknown): Record<string, unknown> {
  expect(value).toBeTypeOf("object");
  expect(value).not.toBeNull();
  expect(Array.isArray(value)).toBe(false);
  return value as Record<string, unknown>;
}

function asArray(value: unknown): unknown[] {
  expect(Array.isArray(value)).toBe(true);
  return value as unknown[];
}

function changedPaths(result: ReconcileResult): string[] {
  return [...result.added, ...result.modified, ...result.removed].map((entry) => entry.path).sort((left, right) => left.localeCompare(right));
}

function manifestPaths(manifest: ManifestFile): string[] {
  return manifest.files.map((entry) => entry.path).sort((left, right) => left.localeCompare(right));
}

