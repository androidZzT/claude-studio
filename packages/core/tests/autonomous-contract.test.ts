  it("locks the Codex-only sync, diff, MCP, context visibility, and manifest contract", async () => {
    const rootDir = await createWorkspace("harness-autonomous-codex-");
    await writeText(rootDir, "AGENTS.md.template", "# Codex autonomous fixture\n");
    await writeText(rootDir, ".codex/config.toml.template", 'inherit = "core"\n');
    await writeText(
      rootDir,
      "harness.yaml",
      `
name: autonomous-codex-only
tools:
  - codex
canonical:
  instructions: ./AGENTS.md.template
  codexConfig: ./.codex/config.toml.template
context:
  no_active_context:
    - path: docs/archive/
      reason: Historical run output.
    - path: docs/research/**
      mode: soft_ignore
mcp:
  servers:
    beta:
      command: node
      args: ["beta.js"]
      env: {}
    alpha:
      command: npx
      args: ["-y", "@modelcontextprotocol/server-filesystem"]
      env:
        A_TOKEN: "\${A_TOKEN}"
adapters:
  codex:
    target: .
`
    );

    const initialDiff = await runDiff(rootDir);
    expect(initialDiff.added.map((entry) => entry.path).sort((left, right) => left.localeCompare(right))).toEqual([
      ".codex/config.toml",
      "AGENTS.md"
    ]);

    const syncResult = await runSync(rootDir);
    expect(syncResult.added.map((entry) => entry.path).sort((left, right) => left.localeCompare(right))).toEqual([
      ".codex/config.toml",
      "AGENTS.md"
    ]);

    const agents = await readText(rootDir, "AGENTS.md");
    const codexConfig = await readText(rootDir, ".codex/config.toml");
    const manifest = await loadManifest(rootDir);
    const afterSyncDiff = await runDiff(rootDir);

    expect(agents).toContain("<!-- generated by harness; do not edit -->");
    expect(agents).toContain("# Codex autonomous fixture");
    expect(codexConfig).toContain('inherit = "core"');
    expect(codexConfig).toContain('default_permissions = "harness-no-active-context"');
    expect(codexConfig).toContain("[permissions.harness-no-active-context.filesystem]");
    expect(codexConfig).toContain('"docs/archive" = "none"');
    expect(codexConfig).toContain('"docs/archive/**" = "none"');
    expect(codexConfig).not.toContain("docs/research");
    expect(codexConfig.indexOf("[mcp_servers.alpha]")).toBeLessThan(codexConfig.indexOf("[mcp_servers.beta]"));
    expect(codexConfig).toContain('args = [ "-y", "@modelcontextprotocol/server-filesystem" ]');
    expect(codexConfig).toContain('env = { A_TOKEN = "${A_TOKEN}" }');
    expect(manifestPaths(manifest)).toEqual([".codex/config.toml", "AGENTS.md"]);
    expect(changedPaths(afterSyncDiff)).toEqual([]);
    expect(afterSyncDiff.unchanged.map((entry) => entry.path).sort((left, right) => left.localeCompare(right))).toEqual(manifestPaths(manifest));
  });

